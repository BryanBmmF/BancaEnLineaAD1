    private void buscarClientejButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarClientejButton1ActionPerformed
        String dpi = this.dpiTextField1.getText();
        this.user = controladorUsuario.buscarUsuarioCliente(dpi, EstadoUsuarioCliente.ACTIVADO);
        if (user != null) {
            mostrarInformacionDeSolicitud(user);
        } else {
            mostrarMensajeDeInformacionNoEncontrada();
        }
    }//GEN-LAST:event_buscarClientejButton1ActionPerformed

    private void cambiarContrasenajButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cambiarContrasenajButton2ActionPerformed
        if (user != null) {
            String contrasena = ControladorUsuarioCliente.generarContrasena();
            String contrasenaEncriptada =Md5.getMD5(contrasena);
            Date fechaCaducidad =user.generarFechaDeCaducidad();
            controladorUsuario.modificarContrasena(user.getDpiCliente(), contrasenaEncriptada,fechaCaducidad);
            //Mandar correo
            controladorUsuario.notificarCambioDeContrasena(contrasena, user.getEmail(), fechaCaducidad, user.getUsuarioCliente());
            //Mensaje de aprobacion
            mostrarMensajeDeCambioDeContrasena();
            user = null;
        } else {
            mostrarMensajeDeUsuarioNoEscogido();
            this.dpiTextField1.setText("");
        }
    }//GEN-LAST:event_cambiarContrasenajButton2ActionPerformed

    private void dpiTextField1KeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_dpiTextField1KeyTyped

        char caracter = evt.getKeyChar();

        // Verificar si la tecla pulsada no es un digito
        if (dpiTextField1.getText().length() == 13) {
            evt.consume();
        } else {
            if (((caracter < '0')
                    || (caracter > '9'))
                    && (caracter != '\b' /*corresponde a BACK_SPACE*/)) {
                evt.consume();  // ignorar el evento de teclado
            }

        }

    }//GEN-LAST:event_dpiTextField1KeyTyped

