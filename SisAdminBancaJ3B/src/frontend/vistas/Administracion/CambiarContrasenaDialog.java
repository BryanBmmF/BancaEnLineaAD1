    private void mostrarInformacionDeSolicitud(UsuarioCliente cliente) {
        this.datosjEditorPane1.setText(
                "<h5><p style=\"color:green;\">USUARIO ENCONTRADO</p></h5>"
                + "<ul>\n"
                + "  <li>DPI: " + cliente.getDpiCliente() + "</li>\n"
                + "  <li>Estado: " + cliente.getEstado().toString() + "</li>\n"
                + "  <li>Id usuario: " + cliente.getUsuarioCliente() + "</li>\n"
                + "  <li>CORREO: " + cliente.getEmail() + "</li>\n"
                + "</ul>"
        );
    }

